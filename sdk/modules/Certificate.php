		$attributes = array("GET" => $get, "POST" => $post);
		$json = $client->getJSONObject("certificate", "remove", $attributes);
		return $json;
	}
	public static function find($client, array $get = array(), array $post = array()) {
		$attributes = array("GET" => $get, "POST" => $post);
		$json = $client->getJSONObject("certificate", "find", $attributes);
		return $json;
	}
